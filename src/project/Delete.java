    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            Registry reg = LocateRegistry.getRegistry("localhost", 1099);
            Interface server = (Interface) reg.lookup("rmi://localhost/service");

            int id = Integer.parseInt(txt_search_id.getText());
            boolean response = server.DeletePatient(id);

            if (response) {
                txt_search_id.setText("");
                JOptionPane.showMessageDialog(this, "Deleted Successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Invalid ID", "Wrong Delete", JOptionPane.ERROR_MESSAGE);
            }

        } catch (RemoteException | NotBoundException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

