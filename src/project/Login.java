    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Registry reg = LocateRegistry.getRegistry("localhost", Registry.REGISTRY_PORT);
            Interface server = (Interface) reg.lookup("rmi://localhost/service");

            String username = user.getText();
            String password = pass.getText();

            boolean response = server.login(username, password);

            if (response) {
                Client.login.setVisible(false);
                Client.home.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid User Name Or Password", "Wrong Login", JOptionPane.ERROR_MESSAGE);
            }

        } catch (RemoteException | NotBoundException ex) {
            System.out.println(ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

