    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Registry reg = LocateRegistry.getRegistry("localhost", 1099);
            Interface server = (Interface) reg.lookup("rmi://localhost/service");

            int id = Integer.parseInt(id_txt.getText());
            String[] response = server.AppointmentCheck(id);

            if (response != null) {
                doctor.setText(response[0]);
                date.setText(response[1]);
                id_txt.setText("");
                Client.home.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid ID", "Wrong Resrvation", JOptionPane.ERROR_MESSAGE);
            }

        } catch (RemoteException | NotBoundException ex) {
            System.out.println(ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

